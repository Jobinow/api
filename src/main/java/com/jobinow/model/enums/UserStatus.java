package com.jobinow.model.enums;

/**
 * Enumeration representing the status of a user (e.g., online or offline).
 *
 * <p>Use this enum to indicate the current status of a user within the application.
 * It provides constants for online and offline status.</p>
 *
 * @version 1.0
 */
public enum UserStatus {
    /**
     * Represents the status when a user is online.
     */
    ONLINE,

    /**
     * Represents the status when a user is offline.
     */
    OFFLINE
}